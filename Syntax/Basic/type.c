#include <stdio.h>

int main() {

    // 8ビット
    char charVar = 'c';
    unsigned char uCharVar = 'u';
    // 16ビット
    short shortVar = -1;
    unsigned short uShortVar = 65535;
    // 16ビットor32ビット
    int intVar = 10;
    unsigned int uIntVar = 100;
    // 64ビット
    long longVar = 1000;
    unsigned long uLongVar = 10000;

    // 小数
    float floatVar = 3.14;
    double doubleVar = 6.28;

    return 0;
}